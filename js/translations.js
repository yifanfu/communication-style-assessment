// Current language
let currentLanguage = 'en';

// Function to get translation
function t(key) {
    return translations[currentLanguage][key] || key;
}

// Function to change language
function changeLanguage(lang) {
    currentLanguage = lang;
    updatePageContent();
    
    // Update language selector
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.value = lang;
    }
    
    // Store language preference
    localStorage.setItem('preferredLanguage', lang);
}

// Function to update all page content
function updatePageContent() {
    // Update navigation
    document.getElementById('overview-nav').textContent = t('overview');
    document.getElementById('assessment-nav').textContent = t('assessment');
    document.getElementById('blue-nav').textContent = t('blueDirect');
    document.getElementById('red-nav').textContent = t('redSpirited');
    document.getElementById('yellow-nav').textContent = t('yellowConsiderate');
    document.getElementById('green-nav').textContent = t('greenSystematic');
    
    // Update all elements with data-translate attribute
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[currentLanguage][key]) {
            element.textContent = translations[currentLanguage][key];
        }
    });
    
    // Update all elements with data-translate-html attribute
    document.querySelectorAll('[data-translate-html]').forEach(element => {
        const key = element.getAttribute('data-translate-html');
        if (translations[currentLanguage][key]) {
            element.innerHTML = translations[currentLanguage][key];
        }
    });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    // Load saved language preference
    const savedLanguage = localStorage.getItem('preferredLanguage') || 'en';
    changeLanguage(savedLanguage);
    
    // Add event listener for language selector
    const languageSelect = document.getElementById('languageSelect');
    if (languageSelect) {
        languageSelect.addEventListener('change', function() {
            changeLanguage(this.value);
        });
    }
});